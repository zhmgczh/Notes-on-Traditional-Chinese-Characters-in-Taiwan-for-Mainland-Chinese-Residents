function sort2(a, b, rule)
{
	if(!rule || !rule.length) return 0;
	for(i=0; i<rule.length; i++)
	{
		var p=1, k=rule[i];
		if(k<0)
		{
			p=-1;
			k=-k;
		}
		k--;
		if(a.length<k-1 || b.length<k-1) continue;
		else if(a[k]<b[k])	return -p;
		else if(a[k]>b[k])	return p;
		
		
	}
	return 0;
}



