function toHTML(s,f)
{
	if(!s) 	return "";
	if(!f)
		s=s.replace(/ /g,"&nbsp;");
	s=s.replace(/&/g,"&amp;")
	.replace(/</g,"&lt;")
	.replace(/>/g,"&gt;")
	.replace(/\r\n/g,"<br>")
	.replace(/\"/g,"&quot;");
	return s;
}
String.prototype.toHTML = function (f) {
  return toHTML(this,f);
}

